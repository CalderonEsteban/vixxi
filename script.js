let cart = JSON.parse(localStorage.getItem('shoppingCart')) || [];

function saveCartToLocalStorage() {
    localStorage.setItem('shoppingCart', JSON.stringify(cart));
}

function updateCartCount() {
    const cartCountElement = document.getElementById('cart-count');
    if (cartCountElement) {
        
        const totalItemsInCart = cart.reduce((total, item) => total + item.quantity, 0);
        if (totalItemsInCart > 0) {
            cartCountElement.textContent = totalItemsInCart;
            cartCountElement.style.display = 'flex'; 
        } else {
            cartCountElement.style.display = 'none'; 
        }
    }
}

function calculateCartTotal() {
    let total = 0;
    cart.forEach(item => {
       
        total += item.Amount * item.quantity;
    });
    return total.toFixed(2); 
}

function renderCart() {
    const cartItemsContainer = document.getElementById('cart-items-container');
    const cartTotalElement = document.getElementById('cart-total');
    if (!cartItemsContainer || !cartTotalElement) return;

    cartItemsContainer.innerHTML = ''; 

    if (cart.length === 0) {
        cartItemsContainer.innerHTML = '<p>El carrito está vacío.</p>';
        cartTotalElement.textContent = `Total: $0.00`;
    } else {
        cart.forEach((item, index) => {
            const cartItemDiv = document.createElement('div');
            cartItemDiv.classList.add('cart-item');
            
            cartItemDiv.setAttribute('data-product-id', item.Id + '-' + item.Name); 
            cartItemDiv.innerHTML = `
                <img src="${item.Img}" alt="${item.Name}" class="cart-item-img">
                <div class="cart-item-details">
                    <h4>${item.Name}</h4>
                    <p>Precio Unitario: $${item.Amount.toFixed(2)}</p>
                    <div class="quantity-controls">
                        <button class="decrement-quantity" data-index="${index}">-</button>
                        <span>${item.quantity}</span>
                        <button class="increment-quantity" data-index="${index}">+</button>
                    </div>
                    <p>Subtotal: $${(item.Amount * item.quantity).toFixed(2)}</p>
                </div>
                <button class="remove-from-cart" data-index="${index}"><i class="bi bi-trash3"></i></button>

            `;
            cartItemsContainer.appendChild(cartItemDiv);
        });
        cartTotalElement.textContent = `Total: $${calculateCartTotal()}`;
    }

    const cartActionsDiv = document.getElementById('cart-actions');
    
    if (cart.length > 0) {
        
        cartActionsDiv.innerHTML = ''; 

        const buyButton = document.createElement('button');
        buyButton.textContent = 'Comprar';
        buyButton.classList.add('btn-comprar');
        buyButton.addEventListener('click', () => {
            alert('¡Gracias por tu compra!');
            cart = []; 
            saveCartToLocalStorage();
            updateCartCount();
            renderCart();
            toggleCartVisibility(); 
        });
        cartActionsDiv.appendChild(buyButton);

        const clearCartButton = document.createElement('button');
        clearCartButton.textContent = 'Vaciar Carrito';
        clearCartButton.classList.add('btn-vaciar-carrito');
        clearCartButton.addEventListener('click', () => {
            if (confirm('¿Estás seguro de que quieres vaciar el carrito?')) {
                cart = []; 
                saveCartToLocalStorage();
                updateCartCount();
                renderCart();
            }
        });
        cartActionsDiv.appendChild(clearCartButton);
    } else {
       
        cartActionsDiv.innerHTML = '';
    }

   
    setupRemoveFromCartButtons();
    setupQuantityButtons(); 
}


function toggleCartVisibility() {
    const cartDropdown = document.getElementById('cart-dropdown');
    if (cartDropdown) {
        cartDropdown.classList.toggle('show');
    }
}

function setupAddToCartButton(button, product) {
    button.addEventListener('click', () => {
       
        const existingItem = cart.find(item => item.Id === product.Id && item.Name === product.Name);

        if (existingItem) {
            existingItem.quantity++; 
        } else {
           
            const productToAdd = { ...product, quantity: 1 };
            cart.push(productToAdd);
        }

        saveCartToLocalStorage();
        updateCartCount();
        renderCart(); 
        alert(`${product.Name} ha sido agregado al carrito.`);
    });
}

function setupRemoveFromCartButtons() {
    const removeButtons = document.querySelectorAll('.remove-from-cart');
    removeButtons.forEach(button => {
        button.onclick = null; 
        button.addEventListener('click', (event) => {
            
            event.stopPropagation(); 

            
            const actualButton = event.target.closest('.remove-from-cart');
            
            if (actualButton) {
                const indexToRemove = actualButton.dataset.index;
                cart.splice(indexToRemove, 1); 
                saveCartToLocalStorage();
                updateCartCount();
                renderCart(); 
            }
        });
    });
}


function setupQuantityButtons() {
    const incrementButtons = document.querySelectorAll('.increment-quantity');
    const decrementButtons = document.querySelectorAll('.decrement-quantity');

    incrementButtons.forEach(button => {
        button.onclick = null; 
        button.addEventListener('click', (event) => {
            const index = event.target.dataset.index;
            incrementQuantity(index);
        });
    });

    decrementButtons.forEach(button => {
        button.onclick = null; 
        button.addEventListener('click', (event) => {
            const index = event.target.dataset.index;
            decrementQuantity(index);
        });
    });
}

function incrementQuantity(index) {
    if (cart[index]) {
        cart[index].quantity++;
        saveCartToLocalStorage();
        updateCartCount();
        renderCart(); 
    }
}

function decrementQuantity(index) {
    if (cart[index]) {
        if (cart[index].quantity > 1) {
            cart[index].quantity--;
        } else {
            
            cart.splice(index, 1);
        }
        saveCartToLocalStorage();
        updateCartCount();
        renderCart();
    }
}




function renderizarCatalogo(catalogo, contenedorId) {
    const productosContainer = document.getElementById(contenedorId);
    if (!productosContainer) return;

    productosContainer.innerHTML = ''; 

    catalogo.forEach(producto => {
        const prendaDestacadaDiv = document.createElement('div');
        prendaDestacadaDiv.classList.add('prendaDestacada');

        const imgElement = document.createElement('img');
        imgElement.src = producto.Img;
        imgElement.alt = producto.Name;

        const detallesOcultablesDiv = document.createElement('div');
        detallesOcultablesDiv.classList.add('detalles-ocultables');

        const tituloProductoH3 = document.createElement('h3');
        tituloProductoH3.classList.add('tituloProducto');
        tituloProductoH3.textContent = producto.Name;

        const descripcionProductoP = document.createElement('p');
        descripcionProductoP.classList.add('descripcionDelProducto');
        descripcionProductoP.textContent = `DESCRIPCION: ${producto.Description}`;

        const precioProductoP = document.createElement('p');
        precioProductoP.classList.add('precioDelProducto');
        precioProductoP.textContent = `PRECIO: $${producto.Amount.toFixed(2)}`; 

        detallesOcultablesDiv.appendChild(tituloProductoH3);
        detallesOcultablesDiv.appendChild(descripcionProductoP);
        detallesOcultablesDiv.appendChild(precioProductoP);

        const botonAgregarCarrito = document.createElement('button');
        botonAgregarCarrito.textContent = 'Agregar al Carrito';
        botonAgregarCarrito.classList.add('btn-agregar-carrito');
        setupAddToCartButton(botonAgregarCarrito, producto);

        const botonVerMas = document.createElement('button');
        botonVerMas.textContent = 'Ver Más';
        botonVerMas.classList.add('btn-ver-mas');

        botonVerMas.addEventListener('click', () => {
            detallesOcultablesDiv.classList.toggle('mostrar');
            if (detallesOcultablesDiv.classList.contains('mostrar')) {
                botonVerMas.textContent = 'Ver Menos';
            } else {
                botonVerMas.textContent = 'Ver Más';
            }
        });

        prendaDestacadaDiv.appendChild(imgElement);
        prendaDestacadaDiv.appendChild(botonVerMas);
        prendaDestacadaDiv.appendChild(detallesOcultablesDiv);
        prendaDestacadaDiv.appendChild(botonAgregarCarrito);

        productosContainer.appendChild(prendaDestacadaDiv);
    });
}


document.addEventListener('DOMContentLoaded', () => {
   
    if (document.getElementById('catalogoPantalones')) {
        renderizarCatalogo(catalogoDePantalones, 'catalogoPantalones');
    }
    if (document.getElementById('catalogoCamisas')) {
        renderizarCatalogo(catalogoDeCamisas, 'catalogoCamisas');
    }
    if (document.getElementById('catalogoZapatos')) {
        renderizarCatalogo(catalogoDeZapatos, 'catalogoZapatos');
    }
    if (document.getElementById('catalogoTrajes')) {
        renderizarCatalogo(catalogoDeTrajes, 'catalogoTrajes');
    }
    if (document.getElementById('catalogoAccesorios')) {
        renderizarCatalogo(catalogoDeAccesorios, 'catalogoAccesorios');
    }
    if (document.getElementById('catalogoDestacados')) {
        renderizarCatalogo(catalogoDeDestacados, 'catalogoDestacados');
        const seccionDestacadas = document.getElementById('catalogoDestacados');
        const tituloDestacadas = document.createElement('h2');
        tituloDestacadas.textContent = 'Prendas Destacadas';
        seccionDestacadas.prepend(tituloDestacadas);
    }

    
    updateCartCount();

   
    const cartButton = document.getElementById('cart-button');
    if (cartButton) {
        cartButton.addEventListener('click', (event) => {
            event.preventDefault();
            toggleCartVisibility();
            renderCart();
        });
    }

    
    document.addEventListener('click', (event) => {
        const cartDropdown = document.getElementById('cart-dropdown');
        const cartButtonElement = document.getElementById('cart-button');
        
        
        if (cartDropdown && !cartDropdown.contains(event.target) &&
            cartButtonElement && !cartButtonElement.contains(event.target) &&
            cartDropdown.classList.contains('show')) {
            
            
            const clickedElement = event.target;
            const isCartInteractionButton = clickedElement.classList.contains('increment-quantity') ||
                                           clickedElement.classList.contains('decrement-quantity') ||
                                           clickedElement.classList.contains('remove-from-cart') ||
                                           clickedElement.classList.contains('btn-comprar') || 
                                           clickedElement.classList.contains('btn-vaciar-carrito');

            
            if (!isCartInteractionButton) {
                cartDropdown.classList.remove('show');
            }
        }
    });
});
